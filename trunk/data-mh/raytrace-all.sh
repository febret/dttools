#!/bin/bash
i=0;
for d in bonney-0?-dive??.dtrt.cfg;
do 
    dives[$i]=$d; 
    i=`expr $i + 1`; 
done;

cmd="../bin/--debug/dtrt"; 
for (( j=2; $j < $i; j+=1 )); 
do 
    ccmd="$cmd ${dives[$j]} >& ${dives[$j]}.log"; 
    echo "********************************************************************************************************************************************************************************************************************************"; 
    $ccmd; 
done
